session_menu   = VTL_USER_DIR .. "/rofi/assets/session-menu.sh"


-- ── Fallback defaults for device-specific globals ──────────────────────
-- Normally set in user_settings.lua (generated by the setup). On a fresh client
-- without that file these would be nil and the config would error; fall back to
-- sane values so Hyprland always starts. The monitor + workspaces are then
-- auto-configured on first boot (see modules/autostart.lua). user_settings.lua,
-- when present, is loaded before this module, so its values always win.
exec_once_daemons  = exec_once_daemons  or {}
start_apps         = start_apps         or {}
quick_app          = quick_app          or {}
cur_theme          = cur_theme          or "Oxygen"
cur_size           = cur_size           or 24
fn_brightness_up   = fn_brightness_up   or "XF86MonBrightnessUp"
fn_brightness_down = fn_brightness_down or "XF86MonBrightnessDown"
fn_play_prev       = fn_play_prev       or "XF86AudioPrev"
fn_play_stop_play  = fn_play_stop_play  or "XF86AudioPlay"
fn_play_next       = fn_play_next       or "XF86AudioNext"
fn_volume_mute     = fn_volume_mute     or "XF86AudioMute"
fn_volume_down     = fn_volume_down     or "XF86AudioLowerVolume"
fn_volume_up       = fn_volume_up       or "XF86AudioRaiseVolume"
